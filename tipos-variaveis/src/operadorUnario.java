public class operadorUnario {
    public static void main(String[] args) {
        
        int numero = 5;
        numero = -numero;

        System.out.println(numero);

        // o resultado e -5 o numero foi alterado na 5 linha do codigo //

        
        numero = numero * -1;

        System.out.println(numero);
        // agora o numero volta o seu valor normal , //
        //o numero voi alterado nonamente na linta 12  do codigo//

    
    }
}
